	INNER JOIN XLA_DISTRIBUTION_LINKS XDL_REC         ON RCTGD_REC.CUST_TRX_LINE_GL_DIST_ID = XDL_REC.SOURCE_DISTRIBUTION_ID_NUM_1 AND RCTGD.EVENT_ID = XDL_REC.EVENT_ID
	INNER JOIN XLA_AE_HEADERS AEH_REC                 ON XDL_REC.AE_HEADER_ID = AEH_REC.AE_HEADER_ID
	INNER JOIN XLA_AE_LINES AEL_REC                   ON XDL_REC.AE_LINE_NUM = AEL_REC.AE_LINE_NUM AND XDL_REC.AE_HEADER_ID = AEL_REC.AE_HEADER_ID
	INNER JOIN GL_CODE_COMBINATIONS_KFV GCC_REC       ON AEL_REC.CODE_COMBINATION_ID = GCC_REC.CODE_COMBINATION_ID
